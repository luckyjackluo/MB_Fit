    const double t11_4 = a[10]*(1*x[10] + 1*x[13]);
    const double t11_5 = a[12]*(1*x[3] + 1*x[6]);
    const double t11_6 = a[13]*(2*x[11]);
    const double t11_7 = a[17]*(1*x[12]);
    const double t11_8 = a[20]*(1*x[0]);
